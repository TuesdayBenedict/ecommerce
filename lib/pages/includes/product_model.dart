class ProductItem {
  final String name;
  final String imageUrl;
  final String description;
  final String category;
  final List reviews;
  final double price;
  final int quantity;
  final double rating;

  ProductItem({
    required this.name,
    required this.imageUrl,
    required this.description,
    required this.category,
    required this.reviews,
    required this.price,
    required this.quantity,
    required this.rating,
  });
}

